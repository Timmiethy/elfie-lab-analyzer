TERMINAL_JOB_STATUSES = {"completed", "partial", "failed", "dead_lettered"}
DEFAULT_EXPECTATION = {
    "expected_upload_status_codes": [200],
    "allowed_job_statuses": ["completed", "partial", "failed", "dead_lettered"],
    "require_artifacts_on_job_statuses": ["completed", "partial"],
    "require_proof_pack": True,
    "expected_detail_substrings": [],
}

_NOT_ASSESSED_ALLOWED_KEYS = frozenset({"raw_label", "reason"})
_NOT_ASSESSED_HARD_MAX = 1000
_NOT_ASSESSED_REASON_VALUES = {reason.value for reason in UnsupportedReason}


